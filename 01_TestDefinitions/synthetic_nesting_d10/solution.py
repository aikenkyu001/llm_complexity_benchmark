class Solution:
    def checkNested(self, x0: int, x1: int, x2: int, x3: int, x4: int, x5: int, x6: int, x7: int, x8: int, x9: int) -> bool:
        if x0 > 0:
            if x1 > 0:
                if x2 > 0:
                    if x3 > 0:
                        if x4 > 0:
                            if x5 > 0:
                                if x6 > 0:
                                    if x7 > 0:
                                        if x8 > 0:
                                            if x9 > 0:
                                                return True
        return False